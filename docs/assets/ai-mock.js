// AI mock — generates canned proposed_patch and creates a GitHub Issue.
// Replace generateMockPatch() with a real LLM call when ready.

const GH_PAT_KEY = 'kma:gh:pat:v1';
const REPO_OWNER = 'MartinoKeedian';
const REPO_NAME = 'keedian-market-analysis';
const ISSUES_KEY = (profileId) => `kma:suggestions:cache:${profileId}:v1`;

export async function submitSuggestion(profile, userPrompt) {
  const patch = generateMockPatch(profile, userPrompt);
  const issueBody = formatIssueBody(profile, userPrompt, patch);
  const title = `[suggestion] ${profile.id}: ${patch[0]?.path || 'general'}`;
  const labels = ['suggestion', 'ai-mock', `profile:${profile.id}`];

  const pat = localStorage.getItem(GH_PAT_KEY);
  if (!pat) {
    cacheLocally(profile.id, { title, body: issueBody, labels, created_at: new Date().toISOString() });
    console.log('[ai-mock] no PAT; suggestion cached locally and logged:', { title, body: issueBody });
    return { ok: false, message: 'No PAT — suggestion cached locally. Set a PAT in Parameters to create real GitHub Issues.' };
  }

  try {
    const res = await fetch(`https://api.github.com/repos/${REPO_OWNER}/${REPO_NAME}/issues`, {
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${pat}`,
        'Accept': 'application/vnd.github+json',
        'X-GitHub-Api-Version': '2022-11-28',
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({ title, body: issueBody, labels }),
    });
    if (!res.ok) {
      const errText = await res.text();
      throw new Error(`GitHub API ${res.status}: ${errText}`);
    }
    const issue = await res.json();
    cacheLocally(profile.id, {
      title,
      body: issueBody,
      labels,
      created_at: issue.created_at,
      issue_number: issue.number,
      issue_url: issue.html_url,
    });
    return { ok: true, message: `Issue #${issue.number} created. ${issue.html_url}` };
  } catch (err) {
    cacheLocally(profile.id, { title, body: issueBody, labels, created_at: new Date().toISOString(), error: err.message });
    return { ok: false, message: `Issue create failed (${err.message}). Cached locally.` };
  }
}

function generateMockPatch(profile, userPrompt) {
  // Naive heuristic: if the prompt mentions a country and a field, target it.
  // Real LLM would replace this. Output shape: array of { path, current, proposed, rationale }.
  const m = profile.market_analysis || {};
  const fields = ['arpu_monthly_usd', 'addressable_pct', 'avg_ticket_usd', 'need_perception', 'hw_gap'];
  const countries = ['CL', 'MX', 'US'];

  const lc = userPrompt.toLowerCase();
  const country =
    countries.find((c) => lc.includes(c.toLowerCase())) ||
    (lc.includes('chile') ? 'CL' : lc.includes('mexico') ? 'MX' : lc.includes('usa') || lc.includes('us') ? 'US' : 'US');
  const field = fields.find((f) => lc.includes(f.split('_')[0])) || fields[0];

  const path =
    field === 'arpu_monthly_usd'
      ? `market_analysis.by_country.${country}.subscription.arpu_monthly_usd`
      : field === 'addressable_pct'
      ? `market_analysis.by_country.${country}.subscription.addressable_pct`
      : field === 'avg_ticket_usd'
      ? `market_analysis.by_country.${country}.implementation.avg_ticket_usd`
      : `market_analysis.feasibility_inputs.${field === 'need_perception' ? 'need_perception' : 'delivery_capacity.' + field}`;

  const current = getPath(profile, path);
  const proposed = current === null || current === undefined ? 100 : Math.round(current * 1.15);

  return [
    {
      path,
      current,
      proposed,
      rationale: 'placeholder — real LLM rationale will appear here when wired',
    },
  ];
}

function formatIssueBody(profile, userPrompt, patch) {
  const patchLines = patch
    .map(
      (p) =>
        `- \`${p.path}\`\n  - current: \`${JSON.stringify(p.current)}\`\n  - proposed: \`${JSON.stringify(p.proposed)}\`\n  - rationale: ${p.rationale}`
    )
    .join('\n');
  return `
**Profile:** \`${profile.id}\` — ${profile.display_name || profile.id}

**User prompt:**

> ${userPrompt.replaceAll('\n', '\n> ')}

**Proposed patch:**

${patchLines}

---
_Generated by ai-mock v0. To apply, edit \`data/profiles/${profile.id}.yml\` accordingly, then close this issue._
`.trim();
}

function getPath(obj, dotted) {
  return dotted.split('.').reduce((o, k) => (o ? o[k] : undefined), obj);
}

function cacheLocally(profileId, entry) {
  try {
    const key = ISSUES_KEY(profileId);
    const existing = JSON.parse(localStorage.getItem(key) || '[]');
    existing.unshift(entry);
    localStorage.setItem(key, JSON.stringify(existing.slice(0, 20)));
  } catch (err) {
    console.warn('Failed to cache suggestion locally:', err);
  }
}

export function renderPendingSuggestions(profileId) {
  const container = document.getElementById('ai-suggestions');
  if (!container) return;
  const cache = JSON.parse(localStorage.getItem(ISSUES_KEY(profileId)) || '[]');
  if (!cache.length) {
    container.innerHTML = '<p class="muted small">No suggestions yet for this profile.</p>';
    return;
  }
  container.innerHTML =
    '<h3>Recent suggestions</h3>' +
    cache
      .map(
        (e) => `
    <details class="suggestion-card">
      <summary>${e.issue_number ? `#${e.issue_number} — ` : ''}${escapeHtml(e.title)} <span class="muted small">${e.created_at}</span></summary>
      <pre>${escapeHtml(e.body)}</pre>
      ${e.issue_url ? `<a href="${e.issue_url}" target="_blank" rel="noopener">Open on GitHub →</a>` : '<span class="muted small">(local only — no PAT was set when created)</span>'}
    </details>
  `
      )
      .join('');
}

function escapeHtml(s) {
  return String(s)
    .replaceAll('&', '&amp;')
    .replaceAll('<', '&lt;')
    .replaceAll('>', '&gt;');
}
